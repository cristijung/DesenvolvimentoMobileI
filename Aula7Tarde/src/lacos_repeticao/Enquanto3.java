package lacos_repeticao;

public class Enquanto3 {

    public static void main(String[] args) {
        //variável x começa com zero na execução do programa
        int x = 0;
        
        //enquanto x for menor do que 45, ele vai imprimir x e adicionar
        //mais um;
        while (x < 45) {
            System.out.println("Contando: " + x);
            //variavel "x" mais o incremento (1);
            x++;
        }
       
    }
    
}
